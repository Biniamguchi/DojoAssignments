function getId(id){return document.getElementById(id);}

function addElem(type,cls,txt,par){
    if(!par){return;}
    let e        = document.createElement(type);
    e.className  = cls;
    e.innerHTML  = txt;

    par.appendChild(e);
    return e;
}

function addList(arr,cls,par){
    for(let i=0;i<arr.length;i++){
        let j  = arr[i];

        let li = addElem("li",cls,j[1],getId(par));
        addElem("p","elemTxt",j[0],li);
        addElem("p","elemTxt",j[2],li);
    }
}

function listNums(){
    for(let i=0;i<19;i++){
        if(i==0){addElem("li","numBox numTop red","Group Period",getId("listT"));}
        else{addElem("li","numBox numTop red",i,getId("listT"));}

        if(i<8 && i!=0){addElem("li","numBox red",i,getId("listL"));}
    }
}
